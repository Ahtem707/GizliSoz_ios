import UIKit

enum AppColor {
    enum Text {
        static let white = UIColor(0xFFFFFF)
        static let primary = UIColor(0x000000)
        static let blue = UIColor(0x0F26A1)
    }
    
    enum Button {
        static let backWhite = UIColor(0xFFFFFF)
        static let backGreen = UIColor(0x2DCD34)
        static let white = UIColor(0xFFFFFF)
        static let primary = UIColor(0x000000)
        static let blue = UIColor(0x0F26A1)
    }
    
    enum Cell {
        static let normal = UIColor(0xFFFFFF)
        static let select = UIColor(0x2DCD34)
    }
    
    enum Keyboard {
        static let normal = UIColor(0xFFFFFF)
        static let select = UIColor(0x2DCD34)
        static let line = UIColor(0x2DCD34)
    }
}
